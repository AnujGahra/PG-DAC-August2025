package runTime.polymorphism;

public class Alto extends Car {
	
	@Override
	public void mileage() {
		System.out.println("Alto mileage");
	}
	
	@Override
	public void speed() {
		System.out.println("Alto speed");
	}
	
	@Override
	public void ai() {
		System.out.println("Alto ai");
	}
	
	@Override
	public void engineType() {
		System.out.println("Alto engineType");
	}
	
	@Override
	public void color() {
		System.out.println("Alto Color");
	}
	
	
}
