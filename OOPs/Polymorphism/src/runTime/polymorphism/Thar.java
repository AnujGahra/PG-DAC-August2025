package runTime.polymorphism;

public class Thar extends Car {
	
	
	@Override
	public void mileage() {
		System.out.println("Thar mileage");
	}
	
	@Override
	public void speed() {
		System.out.println("Thar speed");
	}
	
	@Override
	public void ai() {
		System.out.println("Thar ai");
	}
	
	@Override
	public void engineType() {
		System.out.println("Thar engineType");
	}
	
	@Override
	public void color() {
		System.out.println("Thar Color");
	}
	
	
}
