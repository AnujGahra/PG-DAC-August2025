package runTime.polymorphism;

public class Nano extends Car {
	

	@Override
	public void mileage() {
		System.out.println("Nano mileage");
	}
	
	@Override
	public void speed() {
		System.out.println("Nano speed");
	}
	
	@Override
	public void ai() {
		System.out.println("Nano ai");
	}
	
	@Override
	public void engineType() {
		System.out.println("Nano engineType");
	}
	
	@Override
	public void color() {
		System.out.println("Nano Color");
	}
	
}
